const WISH_POOL = ["wish_001", "wish_002", "wish_003", "wish_004", "wish_005", "wish_006"] as const;
const GREETING_POOL = ["greeting_001", "greeting_002", "greeting_003", "greeting_004"] as const;

function pickRandom<T>(items: readonly T[]): T {
  return items[Math.floor(Math.random() * items.length)];
}

export function pickSantaReplyClipId(points: number, wishlistAdded: boolean): SantaVoiceClipId {
  if (wishlistAdded) {
    return pickRandom(WISH_POOL);
  }
  const normalizedPoint = Math.min(3, Math.max(1, points)) as 1 | 2 | 3;
  return pickRandom(NORMAL_POOL[normalizedPoint]);
}

export function pickGreetingClipId(): SantaVoiceClipId {
  return pickRandom(GREETING_POOL);
}
